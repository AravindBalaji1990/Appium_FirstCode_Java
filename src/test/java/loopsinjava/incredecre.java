package Condition_Loop;

public class incredecre {

	public static void main(String[] args) {
		int a =1;
		
		System.out.println("value : " +a);
		int x = ++a;
		System.out.println(x); 
		System.out.println("value : " +a);
		int y = a++;
		System.out.println(y);
		System.out.println("value : " +a);


	}

}
